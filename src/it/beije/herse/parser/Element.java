package it.beije.herse.parser;

public class Element {
	private String tag;
	private String val;

	public void setTag(String tag){
		this.tag = tag;
	}
	public void setVal(String val){
		this.val = val;
	}
	public String getTag(){
		return this.tag;
	}
	public String getVal(){
		return this.val;
	}

}